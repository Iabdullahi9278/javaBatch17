package class3;

public class E3RelationalOperators {
    public static void main(String[] args) {

        System.out.println(10>15); // is 10 greater than 15=false
        System.out.println(10<15); // is 10 lesser than 15=true
        System.out.println(10==15); // is 10 equal to 15=false
        // == means to compare (equal to)
        System.out.println(10!=15); // is 10 is not equal to 15=true
        System.out.println(10>=15); // is 10 greater than or equal to 15=false
        System.out.println(10<=15); // is 10 less than or equal to 15 is=true or false (Answer is true)




    }

}
